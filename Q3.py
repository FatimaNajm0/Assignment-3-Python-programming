# Define the number of rows for the pattern
rows = 5

# Loop from 1 to 5 (inclusive)
# range(1, 6) means it starts at 1 and stops before 6
for i in range(1, rows + 1):
    # In Python, multiplying a string by a number repeats that string
    # For example, '*' * 3 will result in '***'
    print('*' * i)
